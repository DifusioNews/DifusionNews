

document.addEventListener('DOMContentLoaded', function() {
    var cardLink = document.querySelector('.card-link');
    var videoDetail = document.querySelector('.video-detail');
    var ir_home = document.querySelector('.ir_a_home1');
    

    cardLink.addEventListener('click', function(event) {
      event.preventDefault();
      videoDetail.style.display = 'block';
      videoDetail.classList.add('sweep-in');
    });
    ir_home.addEventListener('click', function(event) {
      event.preventDefault();
      videoDetail.style.display = 'none';
    })
});

